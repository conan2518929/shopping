package com.car.shopping.entity;

public class SystemInfo {
	private String service_tel;
	private String trade_rule;
	private String point_rule;
	private String announcement;
	
	public String getPoint_rule() {
		return point_rule;
	}

	public void setPoint_rule(String point_rule) {
		this.point_rule = point_rule;
	}

	public String getAnnouncement() {
		return announcement;
	}

	public void setAnnouncement(String announcement) {
		this.announcement = announcement;
	}

	public String getService_tel() {
		return service_tel;
	}

	public void setService_tel(String service_tel) {
		this.service_tel = service_tel;
	}

	public String getTrade_rule() {
		return trade_rule;
	}

	public void setTrade_rule(String trade_rule) {
		this.trade_rule = trade_rule;
	}

}
